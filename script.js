function showTheCards() {
  var clutter = "";
  arr.forEach(function (obj) {
    clutter += `<div class="box">
        <img class="cursor-pointer" src="${obj.image}" alt="image" loading="lazy">
        <div class="caption text-black font-semibold">${obj.name}</div>
    </div>`;
  });

  document.querySelector(".container").innerHTML = clutter;
}

function handleSearchFunctionality() {
  var input = document.querySelector("#searchinput");
  var overlay = document.querySelector(".overlay");
  var searchdata = document.querySelector(".searchdata");

  input.addEventListener("focus", function () {
    overlay.style.display = "block";
    searchdata.style.display = "block";
  });

  input.addEventListener("blur", function () {
    overlay.style.display = "none";
    searchdata.style.display = "none";
  });

  input.addEventListener("input", function () {
    const filteredArray = arr.filter((obj) =>
      obj.name.toLowerCase().startsWith(input.value)
    );
    var clutter = "";
    filteredArray.forEach(function (obj) {
      clutter += `<div class="res flex px-8 py-3">
            <i class="ri-search-line font-semibold mr-5"></i>
            <h3 class="font-semibold">${obj.name}</h3>
        </div>`;
    });

    searchdata.innerHTML = clutter;
  });
}

showTheCards();
handleSearchFunctionality();
